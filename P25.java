//25.	Java Programs For Converting Temperature Celsius Into Fahrenheit

public class P25 {
    public static void main(String[] args) {
        float celsius = 32.0f;
        float fahrenheit = (float) (celsius * 9 / 5 + 32);
        System.out.println("Celsius = " + celsius + " Fahrenheit = " + fahrenheit);
    }
}
