//27.	Java Program to Find Square and Cube of a Number (N^1, N^2, N^3)

public class P27
{
    public static void main(String[] args)
    {
        int num = 5;
        int square = num * num;
        int cube = num * num * num;
        System.out.println("Square of " + num + " is " + square);
        System.out.println("Cube of " + num + " is " + cube);
    }
}